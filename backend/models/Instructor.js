module.exports=(sequelize,DataTypes)=>{
    const instructor=sequelize.define("instructor", {
        employee_type:{
            type:DataTypes.STRING,
            allowNull:false,
            validate:{
                notEmpty:true
            },
        },
        
        first_name:{
            type:DataTypes.STRING,
            allowNull:false,
            validate:{
                notEmpty:true
            },
        },
        middle_name:{
            type:DataTypes.STRING,
            allowNull:false,
            validate:{
                notEmpty:true
            },
        },
        last_name:{
            type:DataTypes.STRING,
            allowNull:false,
            validate:{
                notEmpty:true,
            },
        },
        email:{
            type:DataTypes.STRING,
            allowNull:false,
            validate:{
                notEmpty:true,
                isEmail:true,
            },
        },
        phone:{
            type:DataTypes.INTEGER,
            allowNull:false,
            validate:{
                notEmpty:true
            },
        },
        salary:{
            type:DataTypes.DECIMAL(7,4),
            allowNull:false,
            validate:{
                notEmpty:true
            },
        },
        employment_date:{
            type:DataTypes.DATE,
            // allowNull:false,
            validate:{
                notEmpty:true,
        },
    },
        course:{
            type:DataTypes.STRING,
            allowNull:false,
            validate:{
                notEmpty:true,
            },
        },
        registration_number:{
            type:DataTypes.INTEGER,
            allowNull:false,
            validate:{
                notEmpty:true
            },
        } 
    },{
        freezeTableName:true,
        tableName:'instructor',
        underscored:true
    })
    return instructor
}